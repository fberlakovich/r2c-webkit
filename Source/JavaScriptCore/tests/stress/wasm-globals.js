var module = loadWebAssembly("wasm/globals.wasm");

shouldBe(module.getX(), 0);
shouldBe(module.getY(), 0);
shouldBe(module.getZ(), 0);

module.setX(1);

shouldBe(module.getX(), 1);
shouldBe(module.getY(), 0);
shouldBe(module.getZ(), 0);

module.setY(0.1);

shouldBe(module.getX(), 1);
shouldBe(module.getY(), 0.10000000149011612);
shouldBe(module.getZ(), 0);

module.setZ(0.1);

shouldBe(module.getX(), 1);
shouldBe(module.getY(), 0.10000000149011612);
shouldBe(module.getZ(), 0.1);
